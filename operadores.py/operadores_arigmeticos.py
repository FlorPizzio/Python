#Operadores arigmeticos
a= 10
b= 3

#suma +

suma= a + b

print(f'suma:{suma}')

#resta -

resta=a - b
print(f'resta:{resta}')

#multicacion *

multiplicacion= a * b
print(f'multiplicacion:{multiplicacion}')

#division / (tipo float)

division= a / b
print(f'division:{division}')   #se puede agregar cuantos decimales queremos imprimir=print(f'division:{division:.2f}')  

#division // (tipo entera)

division_entera= a // b
print(f'division entera:{division_entera}')

#modulo(%)= residuo de la division
modulo= a % b
print(f'residuo de la division:{modulo}')

#exponente (potrncia) **
exponente= a ** b   
print(f'exponente:{exponente}')
