print(f'***Operadores de comparacion (relacionaleles):***')

a, b= 5, 5

print(f'valores iniciales de a: {a}, valores inicilaes de b: {b}')

#Operador de igualidad ==
resultado= (a == b) #parentesis opcional

print(f'resultado de a == b es: {resultado}')
#operador diferente !=

resultado=a !=b

print(f'resultado de a !=b es: {resultado}')

#operador mayor que >
resultado= a > b
print(f'resultado de a > b es: {resultado}')

#Operador mayor o igual que >=

resultado= a >= b

print(f'resultado de a >= b es: {resultado}')

#Operador menor que <

resultado= a < b

print(f'resultado de a < b es: {resultado}')

#Operadoe menor o igual que <=

resultado= a <= b

print(f'resultado de a <= b es: {resultado}')



